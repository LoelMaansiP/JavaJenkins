
public class MathUtils {
public static int add(int a , int b) {
	int sum = a+b;
	return sum;

}
public static int subtract(int a , int b) {
	int subtract = a-b;
	return subtract;
}
public static int mul(int a , int b) {
	int mul = a*b;
	return mul;
}
public static double div(int a , int b) {
	double div = (double)a/(double)b;
	return div;
}
}
